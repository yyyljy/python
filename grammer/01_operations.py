# 01_operations.py
# 논리 연산자
# and, or, not
print("___and___")
print(True and True)
print(True and False)
print(False and False)
print(False and True)

print("___or___")
print(True or True)
print(True or False)
print(False or False)
print(False or True)

print("___not___")
print(not True)
print(not False)

# in, not in
print("___in___")
print('a' in 'apple')
print(1 in [1,2,3])

# 단축 연산자
print('' or 'Text' or 'Text_2')
print('' and 'Text' or 'Text_2')